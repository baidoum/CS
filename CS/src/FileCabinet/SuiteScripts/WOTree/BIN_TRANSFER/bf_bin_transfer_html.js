    // dans un <script>...</script> inline. Sans ce traitement, un bin
    // label contenant "</script>" casserait le HTML genere et pourrait
    // permettre une injection. On echappe < et > en < / >
    // dans le JSON ; le parser JSON cote browser les decode normalement.
    function safeScriptJson(obj) {
        return JSON.stringify(obj || null)
            .replace(/</g, '\\u003c')
            .replace(/>/g, '\\u003e')
            .replace(/\u2028/g, '\\u2028')
            .replace(/\u2029/g, '\\u2029');
    }

