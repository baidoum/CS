'  function schedulePoll() {',
'    if (state.pollTimer) return;',
'    state.pollTimer = setInterval(pollAllPending, 4000);',
'  }',
'',
'  function pollAllPending() {',
'    if (!state.pendingTasks.length) {',
'      clearInterval(state.pollTimer);',
'      state.pollTimer = null;',
'      return;',
'    }',
'    state.pendingTasks.slice().forEach(function (job) {',
'      job.attempts++;',
'      api("checkStatus", { taskId: job.taskId, stagingFileId: job.stagingFileId }).then(function (res) {',
'        var done = res.status === "COMPLETE" || res.status === "FAILED";',
'        var giveUp = !done && job.attempts > 30;',
'        if (!done && !giveUp) return;',
'',
'        state.pendingTasks = state.pendingTasks.filter(function (j) { return j !== job; });',
'',
'        if (giveUp) {',
