'        var indent = document.createElement("span");',
'        indent.className = "wt-tree-indent";',
'        indent.textContent = "  ".repeat(row.depth - 1) + "\\u2514\\u2500 ";',
'        wrap.appendChild(indent);',
'      }',
'      var label = document.createElement("span");',
'      label.textContent = row.tranId;',
'      wrap.appendChild(label);',
'      if (row.isRoot) {',
'        var badge = document.createElement("span");',
'        badge.className = "wt-root-badge";',
'        badge.textContent = "Racine";',
'        wrap.appendChild(badge);',
'      }',
'      tdTree.appendChild(wrap);',
'      tr.appendChild(tdTree);',
'',
'      tr.appendChild(textCell(row.assemblyItemText));',
'      tr.appendChild(textCell(row.assemblyItemDisplayName));',
'',
