'          if (failed && res.errors && res.errors.length) showDiag(JSON.stringify(res.errors, null, 2));',
'        } else {',
'          toast("La tâche en arrière-plan a échoué à appliquer les modifications.", "error", 9000);',
'        }',
'        loadTree(state.page);',
'      }).catch(function () { /* transient poll error - retry next tick */ });',
'    });',
'  }',
